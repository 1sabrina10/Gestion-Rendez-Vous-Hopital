    private void PboutonActionPerformed(java.awt.event.ActionEvent evt) {
        String name = txtName.getText();
        String age = txtAge.getText();
        String email = txtEmail.getText();
        String tel = txtTel.getText();

        try {
            Registry registry = LocateRegistry.getRegistry("localhost", 1099);
            HospitalReservation service = (HospitalReservation) registry.lookup("HospitalService");

            // Ajouter un patient
            boolean result = service.ajouterPatient(name, age, email, tel);
            if (result) {
                JOptionPane.showMessageDialog(this, "Patient ajouté avec succès !");
            } else {
                JOptionPane.showMessageDialog(this, "Échec de l'ajout du patient.");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

